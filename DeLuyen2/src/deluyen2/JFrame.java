    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String tenSP = txtName.getText();
        String maSp = txtMa.getText();
        String danhmuc = this.cbbDanhMuc.getSelectedItem().toString();
        boolean isTT = this.rdoCH.isSelected();
        String TinhTrang = isTT == true ? "Còn Hàng" : "Hết Hàng";
        if (tenSP.length() == 0 || maSp.length() == 0 || txtGia.getText().length() == 0 || danhmuc.length() == 0) {
            JOptionPane.showMessageDialog(this, "không được để trống", "lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int DonGia;
        try {
            DonGia = Integer.parseInt(txtGia.getText());
            DefaultTableModel dtm = (DefaultTableModel) this.tblSanPham.getModel();
            dtm.setRowCount(0);
            SanPham sp = new SanPham(tenSP, maSp, danhmuc, TinhTrang, DonGia);
            arraySP.add(sp);
            this.hienThiJtable();
            this.KhoiTaoUI();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đơn Giá Không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnThemActionPerformed

