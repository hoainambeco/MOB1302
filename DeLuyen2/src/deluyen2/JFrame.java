    private void KhoiTaoUI() {
        txtName.setText("");
        txtMa.setText("");
        txtGia.setText("");
        rdoCH.setSelected(true);
        cbbDanhMuc.removeAllItems();
        cbbDanhMuc.addItem("Laptop");
        cbbDanhMuc.addItem("SmartPhone");
    }

    private void hienThiJtable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSanPham.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < arraySP.size(); i++) {
            SanPham sp = (SanPham) arraySP.get(i);
            Object[] rowData = new Object[]{
                sp.getTenSP(),
                sp.getMaSP(),
                sp.getDanhMuc(),
                sp.getTinhTrang(),
                sp.getDonGia()
            };
            dtm.addRow(rowData);
        }
    }
