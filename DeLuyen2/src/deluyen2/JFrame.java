    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = this.tblSanPham.getSelectedRow();
        if (row == -1) {
            return;
        }
        this.btnXoaActionPerformed(evt);
        this.btnThemActionPerformed(evt);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = this.tblSanPham.getSelectedRow();
        if (row == -1) {
            return;
        } else {
            this.arraySP.remove(row);
            this.hienThiJtable();
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int row = this.tblSanPham.getSelectedRow();
        SanPham sp = (SanPham) arraySP.get(row);
        if (row == -1) {
            return;
        } else {
            this.txtName.setText(sp.getTenSP());
            this.txtMa.setText(sp.getMaSP());
            this.txtGia.setText(String.valueOf(sp.getDonGia()));
            if (sp.getTinhTrang().equalsIgnoreCase("còn hàng")) {
                this.rdoCH.setSelected(true);
            } else if (sp.getTinhTrang().equalsIgnoreCase("hết hàng")) {
                this.rdoHH.setSelected(true);
            }
            for (int i = 0; i < this.cbbDanhMuc.getItemCount(); i++) {
                if (this.cbbDanhMuc.getItemAt(i)
                        .equals(sp.getDanhMuc())) {
                    this.cbbDanhMuc.setSelectedIndex(i);
                }
            }

        }
    }//GEN-LAST:event_tblSanPhamMouseClicked
