    private void hienThiJtable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSP.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < arraySP.size(); i++) {
            SanPham sp = (SanPham) arraySP.get(i);
            Object[] rowData = new Object[]{
                sp.getTenSP(),
                sp.getMaSP(),
                sp.getDanhMucSP(),
                sp.getGiaSP()
            };
            dtm.addRow(rowData);
        }
    }
    public static Object readObject(String path){
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream(path));
            Object object = ois.readObject();
            ois.close();
            return object;
        } 
        catch (Exception e) {
            e.printStackTrace();
            System.out.println("Đọc dữ liệu thất bại");
        }
        return null;
    }
    public static Object writeObject(String path, Object object){
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(path));
            oos.writeObject(object);
            oos.close();
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Ghi dữ liệu thất bại");
        }
        return null;
    }
