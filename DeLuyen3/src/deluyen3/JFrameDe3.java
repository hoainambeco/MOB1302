    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String tenSP = txtName.getText();
        String maSP = txtID.getText();
        String Danhmuc = this.cbbDanhmuc.getSelectedItem().toString();
        if (tenSP.length() == 0 || maSP.length() == 0 || txtDG.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Bạn đã để trống 1 ô dữ liệu", "lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Double DonGia;
        try {
            DonGia = Double.parseDouble(txtDG.getText().toString());
            if (DonGia <= 0) {
                JOptionPane.showMessageDialog(this, "Giá trị đơn giá phải là ký tự số vào lớn hơn 0");
                return;
            } else {
                DefaultTableModel dtm = (DefaultTableModel) this.tblSP.getModel();
                dtm.setRowCount(0);
                SanPham sp = new SanPham(tenSP, maSP, Danhmuc, DonGia);
                arraySP.add(sp);
                this.hienThiJtable();
                this.KhoiTaoUI();
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Giá trị đơn giá phải là ký tự số vào lớn hơn 0");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = this.tblSP.getSelectedRow();
        if (row == -1) {
            return;
        } else {
            this.arraySP.remove(row);
            this.hienThiJtable();
            this.KhoiTaoUI();
            return;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSPMouseClicked
        // TODO add your handling code here:
        int row = this.tblSP.getSelectedRow();
        SanPham sp = (SanPham) arraySP.get(row);
        if (row == -1) {
            return;
        } else {
            this.txtName.setText(sp.getTenSP());
            this.txtID.setText(sp.getMaSP());
            this.txtDG.setText(String.valueOf(sp.getGiaSP()));
            for (int i = 0; i < this.cbbDanhmuc.getItemCount(); i++) {
                if (this.cbbDanhmuc.getItemAt(i)
                        .equals(sp.getDanhMucSP())) {
                    this.cbbDanhmuc.setSelectedIndex(i);
                }
            }

        }
    }//GEN-LAST:event_tblSPMouseClicked

